package com.example.lib_a

class calculate {

    companion object {
        fun plus(a: Int, b: Int): Int {
            return a + b
        }

        fun minus(a: Int, b: Int): Int {
            return a - b
        }

        fun product(a: Int, b: Int): Int {
            return a * b
        }

        fun divide(a: Int, b: Int): Int {
            return a / b
        }

    }
}
}